# ------------------------------------
# type() => helps you to know type of object.
# All Data Python is (Object)
# ------------------------------------

print(type(10))                    # int

print(type(-50.58))                # float

print(type("shehab"))              # string

print(type([1,2,3,4,5,6]))         # list

print(type((1,2,3,4,5,6)))         # tuple: will be discussed later

print(type({"One": 1, "Two": 2}))  # dictionary
