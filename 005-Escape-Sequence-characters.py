# ------------------------------------------
# \b => Back Space. Eleminate the last character in your string
# \newline => Escape New Line in my code + \
# \\ => Escape Back Slash
# \' => Escape Single Quote
# \" => Escape Double Quote
# \n => Line Feed Character
# \r => Carriage Return , Replace the next characters with the previous characters
# \t => c
# \x => translate hex to it's value
# ------------------------------------------

# will Remove Space
print("Hello \b") 

# Escape NewLine
print("I love \
      you \
      shehab")

# Esacpe Back Slash
print("I will be great \\")

# Escape Single Quote
print('I love Single Quote \'test\' ')

# Escape Double Quote
print("I love Single Quote \"test\" ")

# Line Feed Character
print("Shehab is a great developer, ok?\nYup, need to answer, I am sure.")

# Carriage Return 
print("123456\rABCD")
print("123456\rABCDE")

# Horizontal Tab
print("Shehab\tAldeen")